<?php

namespace Math;


/**
 * Calculator
 *
 * Controls all functions to test Calculator class
 * @package     Math
 * @category	Practice
 * @author  	Nimra
 */

class Calculator
{
    /**
    *add two numbers
    *@param a int
    *@param b int
    */
    public function add($a, $b)
    {
        return $a + $b;
    }

    /**
    *Subtract two numbers
    *@param a int
    *@param b int
    */
    public function sub($a, $b)
    {
        return $a - $b;
    }
}
